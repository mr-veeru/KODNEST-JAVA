
public class ArithmeticOperator 
{
	public static void main(String args[])
	{
		System.out.println("Addition result is  " + (8+6));
		System.out.println("Subtraction result is  " + (8-6));
		System.out.println("Multiplication result is  " + (8*6));
		System.out.println("Division result is  " + (8/6));
		System.out.println("Modulus result is  " + (8%6));
	}
}
